package com.oyourneed.model;

public class ProductModel {

    String product_name;
    String product_image;
    String product_minimum_qty;
    String product_unit;
    String product_price;
    String product_qty;
    String product_total_price;
    String product_temp_price;
    String product_discount;

    public String getProduct_temp_price() {
        return product_temp_price;
    }

    public void setProduct_temp_price(String product_temp_price) {
        this.product_temp_price = product_temp_price;
    }

    public String getProduct_discount() {
        return product_discount;
    }

    public void setProduct_discount(String product_discount) {
        this.product_discount = product_discount;
    }

    public String getProduct_name() {
        return product_name;
    }

    public void setProduct_name(String product_name) {
        this.product_name = product_name;
    }

    public String getProduct_image() {
        return product_image;
    }

    public void setProduct_image(String product_image) {
        this.product_image = product_image;
    }

    public String getProduct_minimum_qty() {
        return product_minimum_qty;
    }

    public void setProduct_minimum_qty(String product_minimum_qty) {
        this.product_minimum_qty = product_minimum_qty;
    }

    public String getProduct_unit() {
        return product_unit;
    }

    public void setProduct_unit(String product_unit) {
        this.product_unit = product_unit;
    }

    public String getProduct_price() {
        return product_price;
    }

    public void setProduct_price(String product_price) {
        this.product_price = product_price;
    }

    public String getProduct_qty() {
        return product_qty;
    }

    public void setProduct_qty(String product_qty) {
        this.product_qty = product_qty;
    }

    public String getProduct_total_price() {
        return product_total_price;
    }

    public void setProduct_total_price(String product_total_price) {
        this.product_total_price = product_total_price;
    }
}
